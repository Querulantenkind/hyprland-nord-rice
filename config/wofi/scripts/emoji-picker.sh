# Function to copy to clipboard
copy_to_clipboard() {
    local emoji="$1"
    echo -n "$emoji" | wl-copy
    echo "$emoji wurde in die Zwischenablage kopiert!"
}

# Show emoji menu with wofi
SELECTED=$(printf '%s\n' "${EMOJIS[@]}" | wofi --dmenu --prompt "Emoji suchen:" --width 600 --height 400 --location center --gtk-dark)

# Extract emoji from selection
if [ -n "$SELECTED" ]; then
    EMOJI=$(echo "$SELECTED" | cut -d' ' -f1)
    copy_to_clipboard "$EMOJI"
fi
